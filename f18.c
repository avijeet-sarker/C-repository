#include <stdio.h>
int main ()
{
    //increment operator
    // ++i pre increment
    // i++ post increment
    int i=1;
   printf("%d\n", i++);
   printf("%d\n", i);
   printf("%d\n", ++i);
   printf("%d\n", i);
   printf("%d\n", i--);
   printf("%d\n", i);
   printf("%d\n", --i);
   printf("%d", i);
    return 0;

}